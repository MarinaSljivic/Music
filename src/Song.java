public class Song {
	private int songID;
	private String title;
	private String artist;
	private String album;
	private int yearReleased;

	public int getSongID() {
		return songID;
	}

	public String getTitle() {
		return title;
	}

	public String getArtist() {
		return artist;
	}

	public String getAlbum() {
		return album;
	}

	public int getYearReleased() {
		return yearReleased;
	}

}
